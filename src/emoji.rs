pub type Emojis = HashMap<String, usize>;

pub fn count(src: &String) -> Emojis {
    let mut map = HashMap::new();

    for emoji in EMOJI.iter() {
        let count = src.matches(emoji).count();
        if count > 0 {
            map.insert(String::from(*emoji), count);
        }
    }

    map
}
